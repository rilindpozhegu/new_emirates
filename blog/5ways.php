    <p class="text-body-blog">From the time we hit adolescence (and maybe even earlier), we live our lives caring what other people think of us. We’re all guilty of this to a certain degree — it’s human nature to want to be liked and approved of by the tribe around us. However, there’s a problem when a fear of criticism or judgment drowns out your inner voice. This fear is one of the biggest reasons why people don’t go after what they want.</p>
    <p class="text-body-blog">The best entrepreneurs are able to put aside the constant need for approval. Do they respect the opinions of others? Certainly. But, the voice in their heads is always louder than every other voice they hear. That’s what enables them to step outside the norm, forge their own path and create something brilliant.</p>
    <p class="text-body-blog">Related: People Hating on You? Here Are 4 Ways to Use That Negative Energy to Your Advantage.</p>
    <p class="text-body-blog">I learned that I was placing too much weight on other people’s opinions the hard way. With my first business, success came easy, but it’s proven to be a much harder journey the second time around. For a long time, I was embarrassed about this fact. I imagined everyone whispering about how I got lucky the first time but can’t really run a company. I worried what people thought: my neighbors, the parents of my kids’ friends, people in the industry and even my husband and business partner.</p>
    <p class="text-body-blog">Caught up with what everyone else might be thinking, I adopted the “fake it until you make it” strategy. I kept our company’s spending going on all six cylinders — until finally our accountant laid it on the line that we were hemorrhaging cash — and if we kept it up, the business wouldn’t last the year. And one of my first thoughts after hearing this was, “Everyone will think I’m a failure.”</p>
    <p class="text-body-blog">I have come a long way since this time. A few scary panic attacks gave the impetus needed to change my thinking. I have come to realize that most people don’t spend their time judging me, or even thinking about me. And those few people out there who might judge my actions and decisions? They have their own issues going on.</p>
    <p class="text-body-blog">This revelation has been incredibly freeing. I’m able to make decisions based on my own core values rather than a vague concern about others. In fact, getting over my fear of embarrassment or failure has been the best thing that could have happened to my business. And here’s my advice for other entrepreneurs or budding entrepreneurs who care too much about what others think.</p>
    <p class="text-body-blog">Related: 7 Mindsets at the Foundation of Entrepreneurial Success 1. Know your own values.</p>
    <p class="text-body-blog">The key is having a firm understanding of what’s important to you and where you want to go. When you keep your own values and vision front-and-center, you’ll naturally put less weight on other people’s opinions. 2. Stop judging yourself.</p>
    <p class="text-body-blog">Next, if you don’t want others to judge you, you need to turn down the volume of your inner critic. Sometimes we hit ourselves over and over again with judgments to the point that we begin to question our own abilities and decision making. 3. Imagine the worst that could possibly happen.</p>
    <p class="text-body-blog">When you find yourself slipping back into old habits of worrying about others, the most helpful strategy is to imagine the worst thing that could happen if someone does judge what you’re doing. You turn the light on when the monster is in the bedroom — and realize it’s not as scary as you once thought. 4. Make decisions quickly, but be slow to change your mind.</p>
    <p class="text-body-blog">When you’re worried about what others think, it’s hard to be decisive. Even the simplest of decisions can take weeks of agonizing contemplation — meanwhile there’s no forward progress. I try to follow the mantra, “Make decisions quickly but be slow to change your mind.” This way, I’ll be more likely to start more journeys, and be less likely to quit. 5. Listen to your customers and clients.</p>
    <p class="text-body-blog">Entrepreneurs still need to pay close attention to what their customers — and potential customers — are thinking. After all, these are the people who write your checks. There’s a big difference between getting over the fear of criticism and ignoring customer and market needs.</p>
    <p class="text-body-blog">The bottom line is that whenever you step too far outside of the box, there’s a chance that you’ll be judged by others. Especially in the early stages of your business or vision, you can expect that family and friends may not understand what you’re trying to do. People will tell you that you’re crazy or wrong or stupid for pursuing your idea. During these times, the best entrepreneurs will dig deep and stick to their core vision and values. When you stop worrying about what others think, then you’re finally able to live life on your own terms.</p>
