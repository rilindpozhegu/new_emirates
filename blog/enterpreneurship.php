    <p class="text-body-blog">Do young people view entrepreneurship as a viable career option? Not according to a recent analysis by The Wall Street Journal that showed that only 3.6 percent of households headed by adults younger than 30 owned stakes in private companies. This figure represents a 24-year-low in young entrepreneurs.</p>
    <p class="text-body-blog">While media reports glamorize successful entrepreneurs such as Facebook CEO Mark Zuckerberg and Snapchat CEO Evan Spiegel, the WSJ article calls young entrepreneurs an “endangered species.”</p>
    <p class="text-body-blog">Competition is tough. Capital is scarce. The environment rapidly changes. Education is expensive.</p>
    <p class="text-body-blog">So what should society be telling young people about entrepreneurship?</p>
    <p class="text-body-blog">In November I was involved in an entrepreneurship discussion at Pathways Academy of Technology and Design in Hartford, Conn., along with some other business owners. The discussion was practical and informative, but afterward I realized that it might have done little to inspire and may have inadvertently scared off the high schoolers by emphasizing initial struggles.</p>
    <p class="text-body-blog">What do young people actually need to hear?</p>
    <p class="text-body-blog">Related: The Joy of Raising a Teen Entrepreneur 1. Youth matters.</p>
    <p class="text-body-blog">It’s been said, “The best time to plant a tree was 20 years ago. The second best time is now.” Many entrepreneurs say they wished they started on their path sooner.</p>
    <p class="text-body-blog">Young people don’t need to wait for an opportunity or idea to hit them. They need to start building their future now.</p>
    <p class="text-body-blog">The sooner you start working toward a goal, the sooner your knowledge, experience and money start compounding. Later on in life, commitments can intrude. 2. Money adds up.</p>
    <p class="text-body-blog">It’s easy to want to divorce notions about launching a business from capital requirements to inspire young people. But it’s better to tell them how much money matters to a business so that they don’t head into life blindfolded.</p>
    <p class="text-body-blog">Many entrepreneurs finance their businesses on their own dime. A credit card can literally mean the difference between success and failure. Money, credit and funding matter to every kind of business. 3. Tenacity is significant.</p>
    <p class="text-body-blog">Young people might view certain businesspeople as somehow inherently different, especially outliers like Apple co-founder Steve Jobs or Tesla Motors CEO Elon Musk. Everyone has ideas, creativity and skills. But not everyone applies them consistently toward goals. Geniuses go broke. Rich people lose their money.</p>
    <p class="text-body-blog">The reality is that people are successful not because of what’s bestowed them but rather what they do with the gifts they have. Young people need to look within, find their strengths and make the most of them. 4. Education carries weight.</p>
    <p class="text-body-blog">Would Mark Zuckerberg have launched Facebook if he had dropped out of community college instead of Harvard? It’s likely that instead of showering his business with massive amounts of capital, top venture capitalists wouldn’t have met with him. The name brand of Harvard matters.</p>
    <p class="text-body-blog">Yet education is just one part of a businessperson’s path. Certain goals require education. Others don’t at all. It’s important to develop a realistic understanding of when and why education plays a role.</p>
    <p class="text-body-blog">Related: Girl Scouts Get Trendy With Gluten-Free, Greek Yogurt Cookies 5. Trends are significant.</p>
    <p class="text-body-blog">Entrepreneurship is now being taught like a subject. High schoolers and college kids around the country are developing ideas for businesses. It’s not surprising that many of these ideas are for mobile apps. But unless you’re launching an app right this second, apps are the past.</p>
    <p class="text-body-blog">In the early days of app development, people developing, say, a fake beer-drinking simulation app probably made a million dollars. But now the competition for apps couldn’t be higher.</p>
    <p class="text-body-blog">Instead of focusing on today’s framework for success, young people should be taught to pounce on and spot trends and think about the next 10 years instead of the past 10. 6. Concrete problems count.</p>
    <p class="text-body-blog">A guy near me opened a business straight out of high school hauling away dirt. Everyone needing their property grounds leveled or cleared called him and he built up a big customer base. He then started selling the dirt.</p>
    <p class="text-body-blog">Now that’s a business model: He got paid twice — and for dirt. Twenty years later, he’s reaped a fortune.</p>
    <p class="text-body-blog">With a media full of Zucks and Musks, young people might think successful businesses build only social-network, apps and rockets. The reality is all over, successful entrepreneurs have figured out how to turn dirt into gold. These stories should be shared. Entrepreneurship, above all else, solves problems. Look for problems and you’ll find answers.</p>
