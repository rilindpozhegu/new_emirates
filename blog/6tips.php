    <p class="text-body-blog">The entrepreneurial journey is ripe with experiences that make you uncomfortable and, at times, even embarrassed. If this is you, here’s something to try: Bring your team (or just yourself) to an improv comedy class. Seriously! I’ve taken a couple of these, and each time I walk away, I recognize some key correlations between the improv stage and my business.</p>
    <p class="text-body-blog">Related: 8 Lessons Improv Comedy Class Taught Me About Entrepreneurship</p>
    <p class="text-body-blog">Improvisation, or improv, is a form of live theater in which the plot, characters and dialogue of a game, scene or story are made up in the moment. The skills of improv translate to many other areas of life, including business, education, the arts and media. Improv teaches performers to work together, support one other, say “yes” to good ideas and move forward toward a common goal.</p>
    <p class="text-body-blog">Businesses, in fact, can use improv training to boost team morale and encourage better collaboration and acceptance of change in the workplace. For most, this starts off as a private improv workshop. There, improv performers share the skills they learned from the stage and show how those skills can help in everyday situations.</p>
    <p class="text-body-blog">I spoke with Kevin Gillese, artistic director for Dad’s Garage, an improv and scripted comedy theater based in Atlanta. Through a combination of classes, corporate workshops, high school outreach and public performances at the theater, performers and staffers at the theater share the power of improv with the Atlanta community.</p>
    <p class="text-body-blog">And it is powerful: Gillese shared with me six key tips from improv that can be applied to business: 1. Let go of your own agenda . . .</p>
    <p class="text-body-blog"> if it does not suit the situation. If someone contributes an idea that takes hold and moves the group away from your own original idea, that is okay. The rule-of-thumb in improv is to say: “Yes, and . . .” to new ideas without trying to drag people back to your old concept. As Gillese explained, “Coming in with a strong idea is great, but if the circumstances demand for your idea to change and you resist that change, you’re dead in the water. Be prepared to let go of your original idea and let things evolve.” 2. Don’t hog the spotlight.</p>
    <p class="text-body-blog">Spend more time listening to other people and building off their ideas, rather than forcing them to listen to you and use your ideas all the time. “Listening to the ideas of others is the foundation for collaboration. If you’re a one-man-band, then disregard this note, but if you need a healthy, functional team, then do the hard work of actually listening to those around you,” says Gillese.</p>
    <p class="text-body-blog">Related: How Improv Comedy Can Seriously Grow Your Business 3. Listen for the word ‘no’ and see if it comes up too much.</p>
    <p class="text-body-blog">Does saying “no” bring down people on your team? If so, say “yes” to good ideas rather than shooting down ideas for not being good enough. That will build team morale. “Being positive creates an atmosphere where anything is possible, and being negative creates an atmosphere where nothing is possible. What kind of atmosphere do you want at your company?” Gillese asks. 4. Never define the end point of a project before you start to work.</p>
    <p class="text-body-blog">If you define the final product in the beginning of a project, you won’t allow yourself the chance to come up with great ideas and smart solutions. Allow yourself flexibility for where a project can end up. “Sometimes you don’t know where something is going until you start. Pivots, redirection and changes of all kinds are a healthy part of the process. Some might see this as chaotic, but what it really means is that the leaders are being organic and letting the real world guide their ideas,” Gillese says. 5. Failure is a chance to grow . . .</p>
    <p class="text-body-blog">. . . and it gives you opportunities to learn from your mistakes. Improv actors don’t step on stage and immediately become confident performers. Getting there requires missteps, flubs and mistakes of all sorts — and that’s okay. Gillese says. “True confidence is not a product of perfection, it comes from being okay with your own flaws and missteps. Do not aspire to be perfect, just get comfortable with yourself and your mistakes and you’ll master true confidence,” he adds.</p>
    <p class="text-body-blog">Related: Take a Page From Hollywood, Improv Your Next Brainstorming Session 6. How can you switch to being a helper, not a hindrance . . .</p>
    <p class="text-body-blog">. . . if most of your job entails being a barrier to people? Practice saying “yes, and . . .” to people, supporting their ideas and being flexible enough to go along with radical concepts. “Great ideas often go unsaid by employees because the company culture isn’t receptive to them,” Gillese shares. “Create a culture that supports the discussion of big ideas even if they ultimately get discarded for something more achievable. If you always immediately shoot down ideas that are radical, then eventually people will stop making them.”</p>
