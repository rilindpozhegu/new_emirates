    <h1>Payment Gateways in the UAE</h1>
    
    <p class="text-body-blog"><i>When setting up an e-commerce website in the UAE, selecting the right payment gateway can be a daunting task for any web developer or business owner. The fact is that a lot of services offer some key features but none will offer every single thing you need. Even in 2017, online stores tend to be the way to go to attract investments in a startup, and to an extent this makes sense. Investors like to see clear revenue streams, profit margins, and tried business plans. Despite a recent, more favorable, outlook toward innovation and creativity, many young entrepreneurs choose to open an e-commerce. As a result, choosing the gateway that is right for your business is essential not only from a functional point of view, but also to hit the sweet spot in terms of costs, and be able to use the gateway as a marketing point.
    </i></p>


    <h2 style="text-align: center;">PayPal</h2>
    <p class="text-body-blog">PayPal is the most recognizable gateway on our list and brings with it years of success, a strong reputation, and unparalleled security. PayPal offers two standard packages for their gateway.The first option is completely free to set up but leads customers through the PayPal website. Every transaction costs the merchant $0.10 in fees. PayPal also offers a package that costs $99 to set up and $25 per month in addition to the transaction fees, but this gives far more options in terms of customization and integration. This allows businesses to make their checkouts as personalized and as unique as their websites and products and is a good choice when looking to strengthen corporate branding. It is important to note that the PayPal gateway fees are in addition to the existing fees on a merchant account, which are 4.4% plus a fixed fee based on currency. PayPal offers some add-on services such as recurring billing for $10 per month, fraud protection for $10 per month and $0.05 per transaction, and buyer authentication for $10 per month and $0.10 per transaction.</p>
    <p class="text-body-blog">PayPal might be a powerful tool for marketing, because of its strong brand and reputation, but the major drawback for the UAE market is the lack of support for the dirham. This means extra charges incurred due to conversions and currency exchange.</p>

    <div class="clearfix"></div>

    <h2 style="text-align: center;">Telr</h2>
    <p class="text-body-blog">Telr has repeatedly been named as one of the UAE’s fastest growing startups in recent years. Founded in 2014, the company’s vision is to enable e-commerce in developing markets.Telr gives an extremely interesting package to other startups in the form of a gateway for a fixed monthly fee of AED349. This is valid for as long as the company generates less than AED20K of monthly volume. There are two more standard packages, with the medium one offering services to companies with revenues in the 20K-50K AED range at AED99 per month and 2.69% + AED1 in transaction fees, and the third one to companies with revenue in the 50K-200K range at AED49 per month and 2.49% + AED0.5 per transaction.</p>
    <p class="text-body-blog">Telr’s advantage over PayPal comes from its ability to process dirham transactions which saves companies money in conversion expenses. Furthermore, Telr is a local company in the UAE and so working with them is not only an advantage from a marketing point of view but it also allows you to support local business.</p>

    <h2 style="text-align: center;">PayTabs</h2>
    <p class="text-body-blog">Another player in the MENA region, PayTabs was established in 2014 and provides a powerful gateway tool to online businesses. They provide integration with popular plugins, express checkout features, SDK integration, and direct API integration. Their cost is transaction based and amounts to 2.99% + $0.5 or 3.33% of the transaction value. The pricing gives interesting options that let customers decide what works better for them based on sale volume and average transaction value.</p>
    <p class="text-body-blog">PayTabs has offices open in four MENA countries and has established itself as a leader in the payment gateway market. They make a reliable and strong choice for companies wishing to sell their products directly online.</p>
    <p class="text-body-blog">The market in the UAE is making a slow transition when it comes to online payment. Most stores have the option of paying online and customers are becoming more and more accepting and trusting of said payment methods. The fact remains, however, that some estimates put the number of online orders that are paid with cash on delivery at almost 50%. This makes it all the more important for companies willing to do business online to set up robust and trustworthy payment gateways that inspire confidence and work flawlessly to satisfy their customers. Luckily, many options are available to merchants in the UAE, and they have been implementing them into their web development as a means of improving their marketing strategies as well as increasing sale volume in an effective manner.</p>

    
